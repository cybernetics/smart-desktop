package com.fs.commons.apps.instance;

public class InstanceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public InstanceException() {
		super();
	}

	public InstanceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InstanceException(String arg0) {
		super(arg0);
	}

	public InstanceException(Throwable arg0) {
		super(arg0);
	}

}
